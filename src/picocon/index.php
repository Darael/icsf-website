					From South Kensington stops, follow the route map from
					<a href="http://g.co/maps/akrjw">South Kensington Station</a>.
				</p>
				<p>
					These stop are served by buses
					C1, 9, 10, 49, 52, 70, 74,
					345, 360, 414, 430, and 452.
				</p>
